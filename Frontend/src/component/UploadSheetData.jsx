import React, { useState } from 'react';
import axios from 'axios';

axios.defaults.baseURL = import.meta.env.VITE_Backend_Url; // Backend URL

export const UploadSheetData = () => {
    const [sheetData, setSheetData] = useState({
        className: "",
        sheetId: "",
        sheetName: "",
        testNumber: ""
    });

    function changeHandler(event) {
        const { name, value } = event.target;
        setSheetData((prev) => ({ ...prev, [name]: value }));
    }

    async function submitHandler(event) {
        event.preventDefault();
        try {
            //console.log(sheetData);
            const token = localStorage.getItem('Token');
            if (!token) {
                console.error("No token found, authentication required!");
                return;
            }


            const response = await axios.post('/addSheetDetails', sheetData, {
                headers: {
                    Authorization: `Bearer ${token}`,
                },
            });

            //console.log("Sheet added successfully:", response.data);
            setSheetData({
                className: "",
                sheetId: "",
                sheetName: "",
                testNumber: ""
            });
        } catch (error) {
            console.error("Error adding sheet:", error.response?.data || error.message);
        }
    }

    return (
<div className="flex p-4 md:p-8 h-auto">
  <form onSubmit={submitHandler} className="w-full">
    <div className="flex flex-col md:flex-row p-1 gap-4">
      {/* Left Section */}
      <div className="flex flex-col p-2 md:p-4 w-full md:w-1/2">
        <div className="mb-4 p-2 md:p-4">
          <label htmlFor="className" className="block text-white text-xl md:text-3xl mb-2">Class</label>
          <select
            id="className"
            name="className"
            className="w-full p-2 rounded-lg bg-[rgba(217,217,217,0.28)] text-white text-xl md:text-3xl"
            value={sheetData.className}
            onChange={changeHandler}
          >
            <option value="" disabled>Select Class</option>
            <option className="bg-gray-500" value="10">Class 10</option>
            <option className="bg-gray-500" value="09">Class 09</option>
            <option className="bg-gray-500" value="08">Class 08</option>
            <option className="bg-gray-500" value="07">Class 07</option>
          </select>
        </div>
        <div className="mb-4 p-2 md:p-4">
          <label htmlFor="sheetId" className="block text-white text-xl md:text-3xl mb-2">Sheet Id</label>
          <input
            type="text"
            id="sheetId"
            name="sheetId"
            className="w-full p-2 rounded-lg bg-[rgba(217,217,217,0.28)] text-white text-xl md:text-3xl"
            value={sheetData.sheetId}
            onChange={changeHandler}
          />
        </div>
      </div>

      {/* Right Section */}
      <div className="flex flex-col justify-center p-2 md:p-4 w-full md:w-1/2">
        <div className="mb-4 p-2 md:p-4">
          <label htmlFor="sheetName" className="block text-white text-xl md:text-3xl mb-2">Sheet Name</label>
          <input
            type="text"
            id="sheetName"
            name="sheetName"
            className="w-full p-2 rounded-lg bg-[rgba(217,217,217,0.28)] text-white text-xl md:text-3xl"
            value={sheetData.sheetName}
            onChange={changeHandler}
          />
        </div>
        <div className="mb-4 p-2 md:p-4">
          <label htmlFor="testNumber" className="block text-white text-xl md:text-3xl mb-2">Test No.</label>
          <input
            type="text"
            id="testNumber"
            name="testNumber"
            className="w-full p-2 rounded-lg bg-[rgba(217,217,217,0.28)] text-white text-xl md:text-3xl"
            value={sheetData.testNumber}
            onChange={changeHandler}
          />
        </div>
      </div>
    </div>

    <div className="text-center mt-4">
      <button type="submit" className="bg-green-600 text-white text-xl md:text-3xl py-2 px-4 rounded-lg">Upload Sheet Details</button>
    </div>
  </form>
</div>

    );
}




















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































